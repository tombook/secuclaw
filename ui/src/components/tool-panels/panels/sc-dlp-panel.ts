  // ========== Section E: Performance Optimization Panel ==========
  @state() private _dpRenderTime: number = 0;
  @state() private _dpMemoryEstimate: number = 0;
  @state() private _dpCacheHits: number = 0;
  @state() private _dpCacheMisses: number = 0;
  @state() private _dpLazyLoadingEnabled: boolean = true;
  @state() private _dpVirtualScrollEnabled: boolean = false;
  @state() private _dpPerfHistory: {timestamp:number;renderMs:number;memoryKb:number;cacheRatio:number}[] = [];
  @state() private _dpDataSetSize: number = 1000;

  private _dpMeasurePerformance(): void {
    const start = performance.now();
    const data = Array.from({ length: this._dpDataSetSize }, (_, i) => ({
      id: i, value: Math.random() * 100, category: ['A', 'B', 'C'][i % 3],
      timestamp: Date.now() - i * 60000
    }));
    const filtered = data.filter(d => d.value > 30).map(d => d.value).sort((a, b) => b - a);
    const end = performance.now();
    this._dpRenderTime = Math.round((end - start) * 100) / 100;
    this._dpMemoryEstimate = Math.round((this._dpDataSetSize * 0.15) * 100) / 100;
    this._dpCacheHits = Math.floor(Math.random() * 80 + 60);
    this._dpCacheMisses = Math.floor(Math.random() * 30 + 10);
    this._dpPerfHistory.push({
      timestamp: Date.now(), renderMs: this._dpRenderTime,
      memoryKb: this._dpMemoryEstimate,
      cacheRatio: this._dpCacheHits / (this._dpCacheHits + this._dpCacheMisses)
    });
    if (this._dpPerfHistory.length > 20) this._dpPerfHistory = this._dpPerfHistory.slice(-20);
  }

  private _dpGetCacheRatio(): number {
    const total = this._dpCacheHits + this._dpCacheMisses;
    return total > 0 ? this._dpCacheHits / total : 0;
  }

  private _dpGetPerfRecommendation(): string {
    if (this._dpRenderTime > 50) return 'High render time detected. Consider enabling virtual scrolling and reducing data set size.';
    if (this._dpGetCacheRatio() < 0.7) return 'Cache hit ratio is low. Review cache invalidation strategy and increase cache TTL.';
    if (this._dpMemoryEstimate > 500) return 'High memory usage. Enable lazy loading and consider pagination for large datasets.';
    if (this._dpDataSetSize > 500 && !this._dpVirtualScrollEnabled) return 'Large dataset detected. Enable virtual scrolling for optimal performance.';
    return 'Performance is within acceptable parameters. Continue monitoring.';
  }

